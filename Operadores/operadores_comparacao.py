saldo = 450
saque = 400

# Igualdade
print(400 == 450) ## Nesse caso, está comparando se saque é igual saldo


# Diferença
print(saque != saldo) ## Questiona se saldo é diferente de saldo


# Maior que / Maior ou Igual
print(saldo > saque) ## Compara se o saldo é maior que saque
print(saldo >= saque) ## Compara se saldo é maior ou igual a saque


# Menor que / Menor ou Igual
print(saldo < saque) # Compara se saldo é menor que saque
print(saldo <= saque) # Compara se saldo é menor ou igual a saque