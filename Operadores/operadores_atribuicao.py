# Atribuição Simples
saldo = 500 # Procedimento com atribuição simples
print(saldo)

# Atribuição com Adição
saldo = 500
saldo += 200 # Procediment com atrbuição e adição
print(saldo)

# Atribuição com Subtração
saldo = 500
saldo -= 100 # Procedimento com atribuição e subtração
print(saldo)

# Atribuição com Multiplicação
saldo = 500
saldo *= 2 # Procedimento com atribuição e multiplicação
print(saldo)

# Atribuição com divisão
saldo = 500
saldo /= 5 # Procedimento de atribuição com divisão
print(saldo)

# Atribuição com divisão inteira
saldo = 500
saldo //= 5 # Procedimento de atribuição com divisão inteira
print(saldo)

# Atribuição com módulo
saldo = 500
saldo %= 480 # Procedimento de atribuição com módulo
print(saldo)

# Atribuição com exponenciação
saldo = 80
saldo **= 2
print(saldo)