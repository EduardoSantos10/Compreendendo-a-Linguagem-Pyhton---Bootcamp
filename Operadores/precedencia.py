# Ordem de Precedência dos Operadores

# 1. Parenteses ()
# 2. Expoentes **
# 3. Multip e Divs * e /, // -> Esquerda p/ direita
# 4. Adição e Subs + e - -> Esquerda p/ direita

######################################

# Exemplos

print(10 - 5 * 2) # Direita p/ esquerda

print((10 - 5) * 2) # Esquerda p/ direita

print(10 ** 2 * 2) # Esquedra p/ direita

print(10 ** (2 * 2)) # Direita p/ esquerda

print(10 / 2 * 4) # Esquerda p/ direita